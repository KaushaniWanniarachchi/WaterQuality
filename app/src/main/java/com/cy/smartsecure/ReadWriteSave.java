package com.cy.smartsecure;

public class ReadWriteSave {

    public String android_id;
    public String tds;
    public String turbidity;
    public String waterQuality;
    public String how;
    public String add;
    public String city;


    public ReadWriteSave(String android_id, String tds, String turbidity, String waterQuality, String how, String add, String city){

        this.android_id = android_id;
        this.tds = tds;
        this.turbidity = turbidity;
        this.waterQuality = waterQuality;
        this.how = how;
        this.add = add;
        this.city = city;


    };





}
